    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        try {
            Loan loan = loanController.getLoan(loans.get(getRow()).getId());
            selectOption(loan, loanController, loanOptions, desktop);
            this.dispose();
        } catch (LoanNotFoundException | IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSelectActionPerformed

    private int getRow() {
        int row = tableLoan.getSelectedRow();
 
        if (row < 0) {
            throw new IllegalArgumentException("Select a row.");
        }
        return row;
    }
    
    private void selectOption(Loan loan, LoanController loanController1, LoanOptions options, javax.swing.JDesktopPane desktop) {
        if (options == LoanOptions.UPDATE) {
            UpdateLoan updateLoan = new UpdateLoan(loanController, userController, bookController, loan, desktop);
            updateLoan.setVisible(true);
            desktop.add(updateLoan);
        } else {
            try {
                loanController1.deleteLoan(loan.getId());
                JOptionPane.showMessageDialog(this, "Emprestimo foi removido com sucesso!");
                this.dispose();
            } catch (ImpossibleToDeleteTheLoanException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }
    
    private void initTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tableLoan.getModel();
        if (!loans.isEmpty()) {
            try {
                loans.forEach((Loan loan) -> {
                tableModel.addRow(new Object[] {loan.getUser().getName(), 
                                                loan.getBook().getTitle(),
                                                loan.getLoanDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")),
                                                loan.getEstimatedDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")),
                                                loan.getLoanStatus().toString()});});
                tableLoan.setModel(tableModel);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Lista de emprestimos vazio.");
        }
    }

