    private int getRow() {
        int row = tableLoan.getSelectedRow();
 
        if (row < 0) {
            throw new IllegalArgumentException("Select a row.");
        }
        return row;
    }
    
    private void initTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tableLoan.getModel();
        if (!loans.isEmpty()) {
            try {
                loans.forEach((Loan loan) -> {
                tableModel.addRow(new Object[] {loan.getUser().getName(), 
                                                loan.getBook().getTitle(),
                                                loan.getLoanDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")),
                                                loan.getEstimatedDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")),
                                                loan.getLoanStatus().toString()});});
                tableLoan.setModel(tableModel);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Lista de emprestimos vazio.");
        }
    }
    
    
    private void btnReturnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnBookActionPerformed
        try {
            LocalDate currentDate = LocalDate.now();
            Loan loan = loanController.getLoan(loans.get(getRow()).getId());
            String date = txtReturnDate.getText();
            
            if (date.isEmpty()) {
                throw new IllegalArgumentException("Illegal parameter.");
            }
            
            LocalDate returnDate = LocalDate.parse(date, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            
            if (!returnDate.isBefore(currentDate)) {
                devolutionController.addDevolution(loan, returnDate);
                double trafficTicket = devolutionController.calculateLateFee(returnDate, loan.getEstimatedDate());
                lblTrafficTicket.setText(Double.toString(trafficTicket));
                JOptionPane.showMessageDialog(this, "Livro devolvido com sucesso!\nA multa do livro foi de: R$" + Double.toString(trafficTicket));
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Digite uma data valida");
            }
        } catch (LoanAlreadyReturned | LoanNotFoundException | UninformedParameterException | IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnReturnBookActionPerformed

