    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        try {
            String name = txtName.getText();
            String email = txtEmail.getText();
            String phone = txtPhone.getText();
            String address = txtAddress.getText();
            
            if (name.isEmpty() || email.isEmpty() || phone.isEmpty() || address.isEmpty()) {
                userController.updateUser(user, null, null, null, null);
            } else {
                userController.updateUser(user, name, email, phone, address);
                JOptionPane.showMessageDialog(this, "O Usuário foi atualizado com sucesso!");
            }
            this.dispose();
        } catch (UninformedParameterException | UserAlreadyExistsException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_submitActionPerformed


